#include "../includes/cub3d.h"

int	ft_listlen(t_mapll *map)
{
	t_mapll	*tmp;
	int		i;

	tmp = map;
	i = 0;
	while (tmp)
	{
		i++;
		tmp = tmp->next;
	}
	return (i);
}
